class PatternVisionTransformer(VisionTransformer):
    def __init__(self, *args, **kwargs):
        super().__init__(*args, **kwargs)
        
        num_patches = self.patch_embed.num_patches
        
        self.N = 4
        
        self.num_pattern = 11 * self.N
        self.pos_embed_ = nn.Parameter(torch.zeros(1, num_patches + 1 + 11 * self.N, self.embed_dim))
        self.pattern = nn.Linear(self.embed_dim, self.num_pattern, bias = False)
        self.matrix = nn.Linear(self.embed_dim, self.num_pattern, bias = False)
        
        trunc_normal_(self.pos_embed_, std=.02)
        self.pattern.apply(self._init_weights)
        self.matrix.apply(self._init_weights)
        

    def forward_features(self, x):
        # taken from https://github.com/rwightman/pytorch-image-models/blob/master/timm/models/vision_transformer.py
        # with slight modifications to add the pattern_token
        B = x.shape[0]
        
        x = self.patch_embed(x)
        num_patch = x.shape[1]

        cls_tokens = self.cls_token.expand(B, -1, -1)  # stole cls_tokens impl from Phil Wang, thanks
        pattern_tokens = self.pattern.weight.expand(B,-1,-1)
        
        x = torch.cat((cls_tokens, x, pattern_tokens), dim=1)
        x = x + self.pos_embed_
        x = self.pos_drop(x)

        for b in range(len(self.blocks)):
            x = self.blocks[b](x)
        
        x = self.norm(x)
        
        logits = torch.diagonal(x[:, -self.num_pattern:] @ self.matrix.weight.T, dim1 = -2, dim2 = -1) # B * num_pattern (32, 11*N)

        # minus
        logits_sigmoid = torch.sigmoid(logits)
        pattern_vector = logits_sigmoid @ self.matrix.weight # (32, 22)@ (22, 768) --> (32, 768)
        pattern_vector = torch.ones((B, self.num_pattern)).cuda() @ self.matrix.weight
        #pattern_vector = (logits_sigmoid>=0.5)*1.0 @ self.matrix.weight
        x[:, 0] = x[:, 0] - pattern_vector
        

        logits = logits.reshape([B, 11, self.N]).mean(2)
        
        return x[:, 0], logits

    def forward(self, x):
        x_cls, logits = self.forward_features(x)
        
        return x_cls, logits

class DistilledVisionTransformer(VisionTransformer):
    def __init__(self, *args, **kwargs):
        super().__init__(*args, **kwargs)
        self.dist_token = nn.Parameter(torch.zeros(1, 1, self.embed_dim))
        num_patches = self.patch_embed.num_patches
        self.pos_embed = nn.Parameter(torch.zeros(1, num_patches + 2, self.embed_dim))
        self.head_dist = nn.Linear(self.embed_dim, self.num_classes) if self.num_classes > 0 else nn.Identity()

        trunc_normal_(self.dist_token, std=.02)
        trunc_normal_(self.pos_embed, std=.02)
        self.head_dist.apply(self._init_weights)

    def forward_features(self, x):
        # taken from https://github.com/rwightman/pytorch-image-models/blob/master/timm/models/vision_transformer.py
        # with slight modifications to add the dist_token
        B = x.shape[0]
        x = self.patch_embed(x)

        cls_tokens = self.cls_token.expand(B, -1, -1)  # stole cls_tokens impl from Phil Wang, thanks
        dist_token = self.dist_token.expand(B, -1, -1)
        x = torch.cat((cls_tokens, dist_token, x), dim=1)

        x = x + self.pos_embed
        x = self.pos_drop(x)

        for blk in self.blocks:
            x = blk(x)

        x = self.norm(x)
        return x[:, 0], x[:, 1]

    def forward(self, x):
        x, x_dist = self.forward_features(x)
        x = self.head(x)
        x_dist = self.head_dist(x_dist)
        if self.training:
            return x, x_dist
        else:
            # during inference, return the average of both classifier predictions
            return (x + x_dist) / 2


